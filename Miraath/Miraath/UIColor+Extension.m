#import "UIColor+Extension.h"

@implementation UIColor (Extension)


+ (UIColor *)offWhiteTextColor {
    return [UIColor colorWithRed:239.0f/255.0f green:239.0f/255.0f blue:239.0f/255.0f alpha:1.0f];
}

+ (UIColor *)offWhiteTextShadowColor {
    return [UIColor colorWithRed:137.0f/255.0f green:137.0f/255.0f blue:137.0f/255.0f alpha:1.0f];
}


+ (UIColor *)peachTextColor{
	return [UIColor colorWithRed:253.0f/255.0f green:198.0f/255.0f blue:137.0f/255.0f alpha:1.0f];
}

+ (UIColor *)peachTextShadowColor{
	return [UIColor colorWithRed:134.0f/255.0f green:108.0f/255.0f blue:80.0f/255.0f alpha:1.0f];
}

@end
